# ─── Design tokens ───
BG_DARK       = RGBColor(0x0B, 0x0D, 0x17)   # deep navy
BG_CARD       = RGBColor(0x12, 0x14, 0x22)   # card surface
ACCENT        = RGBColor(0x63, 0x66, 0xF1)   # indigo-500
ACCENT_LIGHT  = RGBColor(0x81, 0x84, 0xF5)   # lighter indigo
TEXT_WHITE     = RGBColor(0xFF, 0xFF, 0xFF)
TEXT_GRAY      = RGBColor(0x9C, 0xA3, 0xAF)   # gray-400
TEXT_MUTED     = RGBColor(0x6B, 0x72, 0x80)   # gray-500
PURPLE         = RGBColor(0xA8, 0x55, 0xF7)   # violet-500
EMERALD        = RGBColor(0x10, 0xB9, 0x81)   # emerald-500

SLIDE_WIDTH  = Inches(13.333)  # 16:9 widescreen
SLIDE_HEIGHT = Inches(7.5)

FONT_HEADING  = "Segoe UI"
FONT_BODY     = "Segoe UI"


def _set_slide_bg(slide, color):
    """Fill the slide background with a solid colour."""
    bg = slide.background
    fill = bg.fill
    fill.solid()
    fill.fore_color.rgb = color


def _add_shape_rect(slide, left, top, width, height, fill_color, opacity=1.0):
    """Add a filled rectangle (used for accent bars, cards, etc.)."""
    shape = slide.shapes.add_shape(MSO_SHAPE.RECTANGLE, left, top, width, height)
    shape.fill.solid()
    shape.fill.fore_color.rgb = fill_color
    shape.line.fill.background()  # no border
    return shape


def _set_text(text_frame, text, font_size, color, bold=False, font_name=FONT_BODY, align=PP_ALIGN.LEFT):
    """Set formatted text on the first paragraph of a text frame."""
    text_frame.clear()
    text_frame.word_wrap = True
    p = text_frame.paragraphs[0]
    p.text = text
    p.font.size = Pt(font_size)
    p.font.color.rgb = color
    p.font.bold = bold
    p.font.name = font_name
    p.alignment = align


def _add_text_box(slide, left, top, width, height, text, font_size, color,
                  bold=False, font_name=FONT_BODY, align=PP_ALIGN.LEFT):
    """Add a text box with styled text."""
    txBox = slide.shapes.add_textbox(left, top, width, height)
    tf = txBox.text_frame
    tf.word_wrap = True
    _set_text(tf, text, font_size, color, bold, font_name, align)
    return txBox


def _add_bullet_list(slide, left, top, width, height, items, font_size=16, color=TEXT_GRAY):
    """Add a text box containing multiple bullet points."""
    txBox = slide.shapes.add_textbox(left, top, width, height)
    tf = txBox.text_frame
    tf.word_wrap = True

    for i, item in enumerate(items):
        if i == 0:
            p = tf.paragraphs[0]
        else:
            p = tf.add_paragraph()

        p.text = f"▸  {item}"
        p.font.size = Pt(font_size)
        p.font.color.rgb = color
        p.font.name = FONT_BODY
        p.space_after = Pt(10)
        p.alignment = PP_ALIGN.LEFT

    return txBox


def _build_title_slide(prs, title_text, subtitle_text=""):
    """Slide 0: Full-bleed title slide with accent bar."""
    slide = prs.slides.add_slide(prs.slide_layouts[6])  # blank layout
    _set_slide_bg(slide, BG_DARK)

    # Left accent bar
    _add_shape_rect(slide, Inches(0), Inches(0), Inches(0.15), SLIDE_HEIGHT, ACCENT)

    # Title
    _add_text_box(
        slide, Inches(1.2), Inches(2.2), Inches(9), Inches(1.5),
        title_text, font_size=40, color=TEXT_WHITE, bold=True, font_name=FONT_HEADING
    )

    # Subtitle
    if subtitle_text:
        _add_text_box(
            slide, Inches(1.2), Inches(3.8), Inches(8), Inches(0.8),
            subtitle_text, font_size=18, color=TEXT_MUTED
        )

    # Bottom accent line
    _add_shape_rect(slide, Inches(1.2), Inches(6.7), Inches(3), Inches(0.04), ACCENT)

    # Branding
    _add_text_box(
        slide, Inches(1.2), Inches(6.85), Inches(4), Inches(0.5),
        "Generated by ProducHub AI", font_size=11, color=TEXT_MUTED
    )


def _build_content_slide(prs, slide_data, slide_num, total_slides):
    """Standard content slide with title + bullet points."""
    slide = prs.slides.add_slide(prs.slide_layouts[6])  # blank layout
    _set_slide_bg(slide, BG_DARK)

    title = slide_data.get("title", "Untitled")
    points = slide_data.get("points", [])

    # Top accent bar
    _add_shape_rect(slide, Inches(0), Inches(0), SLIDE_WIDTH, Inches(0.06), ACCENT)

    # Slide number badge
    _add_text_box(
        slide, Inches(0.8), Inches(0.45), Inches(1.2), Inches(0.4),
        f"{slide_num:02d} / {total_slides:02d}", font_size=10, color=TEXT_MUTED
    )

    # Title
    _add_text_box(
        slide, Inches(0.8), Inches(0.9), Inches(10), Inches(1),
        title, font_size=30, color=TEXT_WHITE, bold=True, font_name=FONT_HEADING
    )

    # Divider line under title
    _add_shape_rect(slide, Inches(0.8), Inches(1.85), Inches(1.8), Inches(0.03), ACCENT_LIGHT)

    # Bullet points
    if points:
        _add_bullet_list(
            slide, Inches(0.8), Inches(2.3), Inches(10.5), Inches(4.5),
            points, font_size=15, color=TEXT_GRAY
        )

    # Bottom branding bar
    _add_shape_rect(slide, Inches(0), Inches(7.15), SLIDE_WIDTH, Inches(0.35), BG_CARD)
    _add_text_box(
        slide, Inches(0.8), Inches(7.15), Inches(5), Inches(0.35),
        "ProducHub AI — Student Productivity Platform", font_size=9, color=TEXT_MUTED
    )


def _fetch_image_for_slide(image_prompt):
    """Fetch an image from pollinations.ai based on a prompt and return the temp file path."""
    encoded_prompt = urllib.parse.quote(image_prompt)
    url = f"https://image.pollinations.ai/prompt/{encoded_prompt}?width=800&height=800&nologo=true"
    try:
        req = urllib.request.Request(url, headers={'User-Agent': 'Mozilla/5.0'})
        with urllib.request.urlopen(req) as response:
            data = response.read()
            fd, path = tempfile.mkstemp(suffix=".jpg")
            with os.fdopen(fd, 'wb') as f:
                f.write(data)
            return path
    except Exception as e:
        print(f"Failed to fetch image: {e}")
        return None


