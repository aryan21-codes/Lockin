    _add_shape_rect(slide, Inches(0), Inches(7.15), SLIDE_WIDTH, Inches(0.35), BG_CARD)
    _add_text_box(slide, Inches(0.8), Inches(7.15), Inches(5), Inches(0.35), "ProducHub AI — Student Productivity Platform", font_size=9, color=TEXT_MUTED)


