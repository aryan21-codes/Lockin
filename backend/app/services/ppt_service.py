async def generate_ppt_file(prompt_text: str, num_slides: int, user_id: str = "anonymous", model="openai/gpt-4o-mini") -> str:
    """
    Calls OpenAI to get structured PPT contents, compiles them into a
    professionally styled PowerPoint file and returns the local filepath.
    """
    system_prompt = f'''You are an expert presentation creator and master storyteller.
Create a highly engaging, comprehensive, and detailed presentation about: "{prompt_text}".
Provide EXACTLY {num_slides} slides. 

To make it engaging and highly informative, use a variety of slide types:
- "content": Standard title and 4-6 highly detailed bullet points. Each bullet MUST be 1-2 full sentences explaining the concept thoroughly, providing deep insights rather than just short phrases.
- "visual": Title, 3-4 detailed bullet points explaining core concepts, and a vivid "image_prompt" describing a relevant cinematic photo.
- "comparison": Title, and two lists ("left_points" and "right_points") comparing two concepts with in-depth explanations.
- "quote": A highly impactful "quote" and its "author" (or an insightful takeaway formatted as a quote).

Respond ONLY with a JSON object in this extremely strict schema:
{{
    "presentation_title": "Short, Punchy Presentation Title",
    "slides": [
        {{
            "type": "content|visual|comparison|quote",
            "title": "Clean, Punchy Title (for content, visual, comparison)",
            "points": ["Detailed explanatory sentence 1.", "Detailed explanatory sentence 2."],
            "left_points": ["In-depth pro 1.", "In-depth pro 2."],
            "right_points": ["In-depth con 1.", "In-depth con 2."],
            "image_prompt": "A cinematic, highly detailed photo of...",
            "quote": "The future belongs to...",
            "author": "Name"
        }}
    ]
}}
'''

    response = await generate_json_response(
        system_prompt=system_prompt,
        user_prompt=f"Generate {num_slides} highly detailed slides now.",
        model=model,
        max_tokens=6000
    )
    slides_data = response.get("slides", [])
    pres_title = response.get("presentation_title", prompt_text[:60])

    if not slides_data:
        raise ValueError("AI failed to generate structural slide data.")

    # ── Build the presentation ──
    prs = Presentation()
    prs.slide_width = SLIDE_WIDTH
    prs.slide_height = SLIDE_HEIGHT

    # 1. Title slide
    subtitle = f"{len(slides_data)} slides  •  AI-generated study material"
    _build_title_slide(prs, pres_title, subtitle)

    # 2. Content slides
    for idx, slide_data in enumerate(slides_data, start=1):
        slide_type = slide_data.get("type", "content")
        if slide_type == "visual":
            _build_visual_slide(prs, slide_data, idx, len(slides_data))
        elif slide_type == "comparison":
            _build_comparison_slide(prs, slide_data, idx, len(slides_data))
        elif slide_type == "quote":
            _build_quote_slide(prs, slide_data, idx, len(slides_data))
        else:
            _build_content_slide(prs, slide_data, idx, len(slides_data))

    # 3. Closing slide
    _build_closing_slide(prs, pres_title)

    # ── Save ──
    os.makedirs("output", exist_ok=True)
    # Sanitize filename
    safe_name = re.sub(r'[^a-zA-Z0-9_\- ]', '', pres_title)[:40].strip().replace(' ', '_')
    filename = f"presentation_{safe_name}.pptx"
    filepath = os.path.join("output", filename)
    prs.save(filepath)

    # Supabase Tracking
    log_generation(
        user_id=user_id, 
        content_type="ppt_presentation", 
        content_data={"prompt": prompt_text, "num_slides": len(slides_data), "filename": filename}
    )
    
    return filepath
